/**
 * 获取翻译文本
 * Get translation text
 *
 * @param key 翻译键 Translation key
 * @param language 语言代码 Language code
 * @param params 替换参数 Replacement parameters
 * @returns 翻译后的文本 Translated text
 */
export function getTranslation(key: string, language = "zh", params?: Record<string, string | number>): string {
  // 如果语言不存在，使用中文作为后备
  // If language doesn't exist, use Chinese as fallback
  const languagePack = translations[language] || translations.zh

  // 如果翻译键不存在，返回键名
  // If translation key doesn't exist, return the key
  let text = languagePack[key] || key

  // 替换参数
  // Replace parameters
  if (params) {
    Object.entries(params).forEach(([paramKey, paramValue]) => {
      text = text.replace(`{${paramKey}}`, String(paramValue))
    })
  }

  return text
}
